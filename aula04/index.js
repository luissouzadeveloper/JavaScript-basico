// VARIÁVEIS

let nome = 'Raimundo'; // String

console.log( nome + ' nasceu em 1984');
console.log('Em 2000 ' + nome + ' conheceu Maria');
console.log(nome + ' casou-se com Maria em 2012');
console.log('Maria teve um filho com ' + nome + ' em 2015');
console.log('O filho de ' + nome + ' chama-se Eduardo');

let vazio; // Variável declarada mas não inicializada.
vazio = 'Não vazio' // <- Inicializada normalmente.
console.log(vazio); // Mesmo não inicializada, retorna-rá um valor 'undefined'!
vazio = undefined;
console.log(vazio);

